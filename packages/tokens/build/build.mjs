#!/usr/bin/env node
// Builds design token outputs from DTCG sources under ../tokens/.
// Emits:
//   dist/theme.css           — CSS custom properties for two systems:
//                                :root, [data-system="operator-console"] { ... }
//                                [data-system="precision-instrument"] { ... }
//                              System A is the default (on :root). Switch at
//                              runtime by setting data-system on <html> or any
//                              ancestor element.
//   dist/tailwind.tokens.js  — Tailwind theme.extend fragment (CSS-var refs)
//   src/generated.ts         — typed TS exports (CSS-var refs)
//
// Also copies dist/theme.css into the orch-monitor mockups harness so static
// HTML under plugins/dev/scripts/orch-monitor/public/mockups/ can consume
// tokens at a stable URL without its own build step.

import StyleDictionary from "style-dictionary";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");

// ── Custom formats ──────────────────────────────────────────────────────────

// CSS variable names are kebab-case derived from token.path. The `js`
// transformGroup would PascalCase these (diverging from css/variables output),
// so we compute names ourselves to keep JS/TS references aligned with CSS.
const cssVar = (token) => `var(--${token.path.join("-")})`;

// Tailwind theme.extend fragment — references CSS custom properties. Safe for
// any system (A/B) since values are var(--...) lookups that resolve at runtime.
StyleDictionary.registerFormat({
  name: "catalyst/tailwind-theme-extend",
  format({ dictionary }) {
    const colors = {};
    const spacing = {};
    const borderRadius = {};
    const fontSize = {};
    const letterSpacing = {};
    const transitionDuration = {};
    const transitionTimingFunction = {};
    const fontFamily = {};
    const fontWeight = {};

    const lineHeights = {};
    const letterSpacings = {};

    for (const token of dictionary.allTokens) {
      const [group, ...rest] = token.path;
      if (group === "line-height") {
        lineHeights[rest.join("-")] = cssVar(token);
      }
      if (group === "letter-spacing") {
        letterSpacings[rest.join("-")] = cssVar(token);
      }
    }

    for (const token of dictionary.allTokens) {
      const [group, ...rest] = token.path;
      const key = rest.join("-");
      const ref = cssVar(token);

      switch (group) {
        case "color":
          colors[key] = ref;
          break;
        case "spacing":
          spacing[key.replace("0-5", "0.5")] = ref;
          break;
        case "radius":
          borderRadius[key] = ref;
          break;
        case "font-family": {
          const val = String(token.$value ?? token.value);
          fontFamily[key] = val
            .split(",")
            .map((s) => s.trim().replace(/^"|"$/g, ""));
          break;
        }
        case "font-weight":
          fontWeight[key] = ref;
          break;
        case "font-size": {
          const line = lineHeights[key];
          const letter = letterSpacings[key];
          const opts = {};
          if (line) opts.lineHeight = line;
          if (letter) opts.letterSpacing = letter;
          fontSize[key] =
            Object.keys(opts).length > 0 ? [ref, opts] : ref;
          break;
        }
        case "letter-spacing":
          letterSpacing[key] = ref;
          break;
        case "motion":
          if (rest[0] === "duration") {
            transitionDuration[rest[1]] = ref;
          } else if (rest[0] === "easing") {
            transitionTimingFunction[rest[1]] = ref;
          }
          break;
        default:
          break;
      }
    }

    const body = {
      colors,
      spacing,
      borderRadius,
      fontFamily,
      fontWeight,
      fontSize,
      letterSpacing,
      transitionDuration,
      transitionTimingFunction,
    };

    return [
      "// Auto-generated by @catalyst/tokens. Do not edit.",
      "/** @type {Partial<import('tailwindcss').Config['theme']>} */",
      `export default ${JSON.stringify(body, null, 2)};`,
      "",
    ].join("\n");
  },
});

// Typed TypeScript exports — each token becomes a const referencing its CSS var.
// Also emits a nested `tokens` object tree and a `Tokens` type.
StyleDictionary.registerFormat({
  name: "catalyst/typescript-consts",
  format({ dictionary }) {
    const lines = [
      "// Auto-generated by @catalyst/tokens. Do not edit.",
      "",
    ];

    const toIdent = (parts) => {
      let s = parts.join("-");
      // Segments starting with a digit get a leading underscore so the
      // identifier stays valid JavaScript (e.g., spacing.0-5 → spacing_0_5).
      s = s.replace(/-/g, "_").replace(/\./g, "_");
      if (/^\d/.test(s)) s = "_" + s;
      s = s.replace(/_([a-z0-9])/g, (_, c) => c.toUpperCase());
      return s;
    };

    const constNames = new Map();
    for (const token of dictionary.allTokens) {
      const ident = toIdent(token.path);
      constNames.set(token.name, ident);
      lines.push(
        `export const ${ident} = "${cssVar(token)}" as const;`,
      );
    }

    // Build nested object literal from paths.
    const tree = {};
    for (const token of dictionary.allTokens) {
      let node = tree;
      for (let i = 0; i < token.path.length - 1; i++) {
        const seg = token.path[i];
        node[seg] ??= {};
        node = node[seg];
      }
      node[token.path.at(-1)] = { __const: constNames.get(token.name) };
    }

    const render = (node, indent = 2) => {
      const pad = " ".repeat(indent);
      const padOuter = " ".repeat(indent - 2);
      const parts = [];
      for (const [key, val] of Object.entries(node)) {
        const safeKey = /^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(key)
          ? key
          : JSON.stringify(key);
        if (val && typeof val === "object" && "__const" in val) {
          parts.push(`${pad}${safeKey}: ${val.__const}`);
        } else {
          parts.push(`${pad}${safeKey}: ${render(val, indent + 2)}`);
        }
      }
      return `{\n${parts.join(",\n")},\n${padOuter}}`;
    };

    lines.push("");
    lines.push(`export const tokens = ${render(tree)} as const;`);
    lines.push("");
    lines.push("export type Tokens = typeof tokens;");
    lines.push("");

    return lines.join("\n");
  },
});

// ── Pass A: operator-console (default) — emits all three outputs ───────────
const systemASd = new StyleDictionary({
  source: [
    path.join(ROOT, "tokens/base.json"),
    path.join(ROOT, "tokens/operator-console.json"),
  ],
  platforms: {
    "css-system-a": {
      transformGroup: "css",
      buildPath: path.join(ROOT, "dist") + "/",
      files: [
        {
          destination: "_system.operator-console.css",
          format: "css/variables",
          options: {
            selector: ':root, [data-system="operator-console"]',
            outputReferences: true,
          },
        },
      ],
    },
    tailwind: {
      transformGroup: "js",
      buildPath: path.join(ROOT, "dist") + "/",
      files: [
        {
          destination: "tailwind.tokens.js",
          format: "catalyst/tailwind-theme-extend",
        },
      ],
    },
    ts: {
      transformGroup: "js",
      buildPath: path.join(ROOT, "src") + "/",
      files: [
        {
          destination: "generated.ts",
          format: "catalyst/typescript-consts",
        },
      ],
    },
  },
});

await systemASd.hasInitialized;
await systemASd.buildAllPlatforms();

// ── Pass B: precision-instrument — CSS only under [data-system="..."] ──────
const systemBSd = new StyleDictionary({
  source: [path.join(ROOT, "tokens/precision-instrument.json")],
  platforms: {
    "css-system-b": {
      transformGroup: "css",
      buildPath: path.join(ROOT, "dist") + "/",
      files: [
        {
          destination: "_system.precision-instrument.css",
          format: "css/variables",
          options: {
            selector: '[data-system="precision-instrument"]',
            outputReferences: true,
          },
        },
      ],
    },
  },
});

await systemBSd.hasInitialized;
await systemBSd.buildAllPlatforms();

// ── Merge: concatenate both system CSS files into a single theme.css ────────
const distDir = path.join(ROOT, "dist");
const systemAPath = path.join(distDir, "_system.operator-console.css");
const systemBPath = path.join(distDir, "_system.precision-instrument.css");
const systemACss = await fs.readFile(systemAPath, "utf-8");
const systemBCss = await fs.readFile(systemBPath, "utf-8");

await fs.writeFile(
  path.join(distDir, "theme.css"),
  `/* Auto-generated by @catalyst/tokens. Do not edit. */\n\n${systemACss}\n${systemBCss}`,
);
await fs.rm(systemAPath);
await fs.rm(systemBPath);

// Copy theme.css into the orch-monitor mockups harness so static HTML under
// plugins/dev/scripts/orch-monitor/public/mockups/ can consume tokens at a
// stable URL as a first-class output of the tokens package (same cadence, same
// source of truth) rather than drifting.
const mockupHarnessPath = path.resolve(
  ROOT,
  "../../plugins/dev/scripts/orch-monitor/public/mockups/_shared/tokens.css",
);
await fs.mkdir(path.dirname(mockupHarnessPath), { recursive: true });
await fs.copyFile(path.join(distDir, "theme.css"), mockupHarnessPath);

console.log(
  "✓ @catalyst/tokens: wrote dist/theme.css, dist/tailwind.tokens.js, src/generated.ts, plugins/dev/scripts/orch-monitor/public/mockups/_shared/tokens.css",
);
