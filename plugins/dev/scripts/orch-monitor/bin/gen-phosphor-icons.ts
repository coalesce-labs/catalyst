#!/usr/bin/env bun
// Regenerates ui/src/lib/phosphor-icon-index.generated.ts and
// ui/src/lib/phosphor-icon-importers.generated.ts from the installed
// @phosphor-icons/react dist/csr filenames. Usage: bun run gen:icons
import { readdirSync, writeFileSync } from "fs";
import { join } from "path";
import { pascalToKebab } from "../ui/src/lib/phosphor-icons.ts";

const CSR_DIR = join(import.meta.dir, "../ui/node_modules/@phosphor-icons/react/dist/csr");
const pascals = readdirSync(CSR_DIR)
  .filter((f) => f.endsWith(".es.js"))
  .map((f) => f.replace(/\.es\.js$/, ""));

// kebab name index (sorted, de-duped)
const names = [...new Set(pascals.map(pascalToKebab))].sort();
const namesBody = names.map((n) => `  ${JSON.stringify(n)},`).join("\n");
writeFileSync(
  join(import.meta.dir, "../ui/src/lib/phosphor-icon-index.generated.ts"),
  `// GENERATED by bin/gen-phosphor-icons.ts — DO NOT EDIT. Run \`bun run gen:icons\` after\n` +
    `// bumping @phosphor-icons/react. Static kebab name index for instant search (CTL-1249).\n\n` +
    `/** All ${names.length} Phosphor kebab icon names, sorted. Feeds buildAllGlyphItems. */\n` +
    `export const PHOSPHOR_ICON_NAMES: readonly string[] = [\n${namesBody}\n];\n`,
  "utf8",
);

// per-icon importer map: kebab -> () => import("@phosphor-icons/react/dist/csr/<Pascal>") (NO suffix)
const importsBody = [...new Set(pascals)]
  .map((p) => `  ${JSON.stringify(pascalToKebab(p))}: () => import("@phosphor-icons/react/dist/csr/${p}"),`)
  .sort()
  .join("\n");
writeFileSync(
  join(import.meta.dir, "../ui/src/lib/phosphor-icon-importers.generated.ts"),
  `// GENERATED by bin/gen-phosphor-icons.ts — DO NOT EDIT. Run \`bun run gen:icons\`.\n` +
    `// Per-icon lazy importers (CTL-1249). Each thunk is a separate Vite chunk (~6-12 KB).\n` +
    `// Subpath omits .es.js — the package exports map appends it.\n\n` +
    `export const ICON_IMPORTERS: Record<string, () => Promise<Record<string, unknown>>> = {\n${importsBody}\n};\n`,
  "utf8",
);

console.info(`Generated ${names.length} icon names + importers.`);
